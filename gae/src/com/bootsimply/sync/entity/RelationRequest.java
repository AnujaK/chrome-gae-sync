package com.bootsimply.sync.entity;

public class RelationRequest {
	
	private RelationFrom from;
	private RelationTo to;
	
	public RelationFrom getFrom() {
		return from;
	}

	public void setFrom(RelationFrom from) {
		this.from = from;
	}

	public RelationTo getTo() {
		return to;
	}

	public void setTo(RelationTo to) {
		this.to = to;
	}

	

}
